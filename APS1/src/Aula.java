import java.util.Date;

public class Aula implements Faltas {
	
	private int numAula;
	private Date dia = new Date() ;
	
	
	
	
// GETTERS E SETTERS	
	public int getNumAula() {
		return numAula;
	}
	public void setNumAula(int numAula) {
		this.numAula = numAula;
	}
	public Date getDia() {
		return dia;
	}
	public void setDia(Date dia) {
		this.dia = dia;
	}

	
	


}
